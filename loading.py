def tick(elapsed: float, timeout: float) -> None:
    """每次轮询调用一次，打印一行荒诞进度条。

    进度策略：前 80% 时间跑完 90% 进度，之后永远卡在 90-95%，
    给人「快好了」的错觉，而不是「怎么还没完」的绝望。
    """
    ratio = elapsed / timeout
    fake_pct = min(int(ratio * 90), 95) if ratio < 0.8 else random.randint(90, 95)
    filled = fake_pct // 10
    bar = "■" * filled + "□" * (10 - filled)
    msg = random.choice(_MESSAGES)
    print(f"  [{bar}] {fake_pct}%  {msg}")
